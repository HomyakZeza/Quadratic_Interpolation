public class AB {
    private double a;
    private double b;

    public AB(double a, double b) {
        this.b = b;
        this.a = a;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public void setA(double a) {
        this.a = a;
    }

    public void setB(double b) {
        this.b = b;
    }
}
